Détails sur les premiers blinds
Quand vous vous asseyez pour la première fois à une table de hold’em, les règles varient quant à déterminer si vous devez ou non poster des blinds (même en étant hors position des blinds) pour recevoir des cartes.

 

À Las Vegas, vous recevez des cartes dès que vous vous asseyez et que vous montrez que vous allez payer le buy-in minimum. Vous n’avez pas à payer de blinds pour recevoir des cartes privatives. C’est aussi le cas dans la plupart des cercles et casinos européens.

 

Inversement, dans la plupart des clubs de Californie, vous devez payer un surblind, quelle que soit votre position, pour recevoir des cartes. C’est aussi le cas dans la plupart des clubs de poker en ligne.

 

Si vous devez payer un surblind avant de recevoir vos cartes, vous avez intérêt à signaler au donneur que vous attendez d’être en position de surblind pour payer. En plus d’éviter de payer une mise forcée, cela vous permet de profiter de quelques minutes d’attente pour observer vos adversaires et essayer de glaner quelques indices. Les donneurs ont l’habitude de cette attitude et ils n’essaieront jamais de vous forcer à payer le surblind hors position.

 

Remarquez cependant que le moment où vous payez le surblind n’a finalement pas grande importance. Si vous êtes impatient de jouer ou si votre temps de jeu est réduit, vous avez tout intérêt à entrer tout de suite dans le jeu, quitte à payer un surblind hors position. Le donneur vous dira si vous devez ou non le miser.

Enchéris
Une enchère est une mise faite par un joueur. Au hold’em, il y a quatre tours d’enchères successifs. Ces tours d’enchères interviennent toujours après que les joueurs ont vu leurs cartes: après les cartes privatives, après le flop, après la turn et après la river.

 

Quand c’est à un joueur de parler, il est devant un certain choix d’annonces. Si le tour d’enchères ne comporte aucune enchère, il peut:

 Checker: Ne pas miser pour l’instant tout en restant dans le coup.
 Ouvrir: Miser une somme.
Si le tour d’enchères comporte déjà au moins une enchère, il peut:

 Passer: Il jette sa main et perd toute prétention au pot.
 Suivre: Il mise une somme égale à l’enchère en cours.
 Relancer: Il mise une somme supérieure à l’enchère en cours en veillant à ne pas faire de string raise (pour plus de détails, voir «L’étiquette du poker», plus loin dans ce chapitre). Pour se maintenir dans le coup après ce joueur, les adversaires doivent au moins miser à cette nouvelle hauteur d’enchères.
Un tour d’enchères s’arrête quand tous les joueurs ont payé le même montant (à l’exception du all-in, voir détails au chapitre 2) après avoir eu l’occasion de parler.

 

Le montant et le type d’enchère sont choisis avant que la partie ne commence. Il existe quatre types d’enchères: spread limit, limites fixes, pot limit et no limit.

Le hold’em en spread limit
Dans les parties en spread limit, vous pouvez miser un montant compris dans une fourchette définie au départ. Par exemple, dans une partie à 1-5 euros, vous pouvez miser 1, 2, 3, 4 ou 5 euros.

 

L’enchère doit toujours être au moins égale à celle qui précède. Si quelqu’un a ouvert à 3 euros, si vous voulez relancer, vous ne pouvez le faire que de 3, 4 ou 5 euros (dans un spread limit 1-5). Si vous débarquez à une table de ce type, n’hésitez pas à demander quelques détails au donneur.

 

Il n’y a guère que sur les tables de cuisine qu’on trouve encore du spread limit. Les cercles et casinos qui proposent ce type d’enchère sont rares. On les trouve surtout dans des coins perdus des États-Unis.

 

Habituellement, le buy-in minimum (la somme avec laquelle vous démarrez la partie) est de dix fois la limite basse. Donc, dans un spread limit 1-5 euros, vous devez vous asseoir avec au moins 10 euros. Il n’y a pas de maximum.

Le hold’em à limites fixes
Les Américains l’appellent limit hold’em. Quand vous entrez dans une partie d’argent ou cash game, située outre-Atlantique, c’est-à-dire une partie dans laquelle vous jouez directement des jetons avec une valeur faciale, il s’agira la plupart du temps d’une partie à limites fixes. En Europe, il s’agira plutôt d’une partie en pot limit ou en no limit. Les limites fixes, comme leur nom l’indique, sont connues à l’avance par tous: quand vous relancez, c’est forcément à une hauteur connue d’avance.

 

Les limites fixes sont au nombre de deux et la plus haute vaut deux fois la plus basse. Par exemple, 10-20 euros.

 

La petite limite vaut pour les deux premiers tours d’enchères: préflop et au flop. Le surblind est toujours égal à cette limite et le petit blind toujours égal à la moitié de cette limite. Par exemple, dans la partie 2-4 euros, le petit blind vaut 1 euro et le surblind vaut 2 euros.

 

La limite haute vaut pour les deux derniers tours d’enchères: turn et river.

 

Dans les casinos et les cercles, on trouve des parties à limites fixes à 2-4 et d’autres à 100-200 ou 1000-2000, voire plus (voir détails au chapitre 16). Les clubs en ligne sont extrêmes eux aussi, mais dans l’autre direction. Ils pratiquent ce qu’on appelle des «micro-limites», aussi basses que 0,05-0,10 dollar (pour plus de détails sur le poker en ligne, voyez le chapitre 15).

 

Le buy-in des parties à limites fixes est généralement égal à dix fois la petite limite. Par exemple, dans la partie à 2-4 euros, le buy-in est de 20 euros. Il n’y a pas de maximum.

Les limites fixes s’avèrent être le type d’enchère qui handicape le plus les bluffeurs (ceux qui tendent à jouer leur main comme si elle était très forte afin de faire passer les autres) parce que les montants des relances sont plafonnés. (Pour plus de détails sur le bluff, voyez le chapitre 9.)

Le hold’em pot limit
Le pot limit est le type d’enchère le plus rare aux États-Unis, mais il est répandu dans les cercles européens qui le pratiquent depuis des décennies.

 

Comme les limites fixes, le pot limit s’exprime avec deux chiffres, par exemple 2-4 euros. Le buy-in est typiquement égal à dix fois le plus petit des deux, donc dans notre exemple, il sera de 10 euros.

 

En pot limit, la quantité que vous avez le droit de miser est la même qu’en limites fixes, mais la relance est égale à la hauteur du pot. Par exemple, si vous jouez dans une partie de pot limit 1-2 euros, et si vous êtes UTG, les blinds sont 1-2 euros et si vous voulez suivre, vous ne payez que 2 euros, comme en limites fixes.

 

Si vous voulez relancer, votre relance est comprise entre deux fois le surblind et la hauteur du pot.

 

Dans notre exemple, votre relance maximum (au pot) se calcule en misant d’abord pour suivre, puis en relançant du tout. Donc vous misez 2 pour suivre et vous relancez de tout le pot, qui est à ce moment égal à 5 (1 + 2 + 2). Donc votre relance maximum sera à 7. Autrement dit, si vous désirez relancer ici, vous miserez entre 4 et 7.

 

Admettons que vous relanciez au maximum 7. Si le joueur suivant veut à son tour relancer au maximum, même procédure: il doit miser 7 pour suivre, puis relancer du tout, soit 2 + 5 + 7 + 7 = 21. Son enchère va donc être de 28 (= 7 + 21).

 

Comme vous le voyez, les prix montent vite. Et avec ces calculs, il faut avoir l’habitude d’y jouer souvent.

Le hold’em no limit
On joue au Texas hold’em depuis le début du XXe siècle et à la version no limit du jeu depuis 1925. Mais encore dans les années 1990, cette forme de poker n’était pratiquée que par quelques joueurs particulièrement atteints. Quant à l’apprendre, comme personne ne vous donnait de leçons, c’était juste une question de grosseur de compte en banque.

 

De nos jours, grâce aux émissions télé, du World Poker Tour à l’European Poker Tour en passant par High Stakes, le no limit est la version la plus populaire dans le monde du hold’em, et même du poker tout court. Un sondage de 2006 montre qu’il est le jeu favori de 60 % des joueurs.

 

Le hold’em no limit est un jeu à la fois vicieux et diabolique dont les règles se résument à peu près à «prends l’oseille à n’importe quel moment».

Une table de cash games de hold’em no limit se note comme en limites fixes, par exemple «1-2 euros». Il s’agit du montant des blinds. Cela signifie que le joueur, quand c’est à lui de parler, peut miser la somme de son choix entre 2 (le surblind) et le total de son tapis.

 

Le buy-in est généralement compris entre dix fois le petit blind et cent fois le gros blind, c’est-à-dire, dans notre exemple, entre 10 et 200 euros. Vous trouverez aussi le buy-in décrit comme «table à 200», ce qui signifie que vous ne pouvez pas vous asseoir avec plus de 200 euros. Pour avoir plus de précisions, demandez au donneur ou au superviseur des tables.

 

D’un point de vue stratégique, il est toujours plus valable de s’asseoir avec le buy-in maximum. Ce jeu étant un jeu d’intimidation, c’est vous qui intimiderez les autres avec un tapis plus fort. En revanche, si plusieurs joueurs, du fait des avatars du jeu, possèdent des tapis au moins égaux au double du maximum de tapis d’entrée, il vaut mieux choisir une autre table car même avec le buy-in maximum, vous restez celui qui se fera intimider.

Quand vous êtes à une table de hold’em no limit, vous n’avez pas le droit de retirer des jetons de votre tapis. Si à un moment donné, vous avez accumulé des gains, vous n’avez donc pas le droit d’en empocher une partie. Vous ne pourrez le faire que quand vous quitterez la table définitivement. Et s’il vous vient l’idée de vous rasseoir à la table, vous devrez le faire avec la quantité de jetons que vous aviez quand vous l’avez quittée, en tout cas tant qu’un délai n’est pas dépassé, qui peut être de 30 ou 60 minutes.

Si vous ne l’aviez pas encore remarqué, le hold’em no limit est une forme particulièrement dangereuse de poker. Tout comme le trapèze pratiqué sans filet, l’absence de limites ne rend pas les figures plus difficiles, mais rend les erreurs plus cuisantes. Démarrer votre carrière de joueur de poker avec du hold’em no limit est le meilleur moyen de vider en un temps record votre capital jeu.

Si vous désirez apprendre le no limit, commencez donc à pratiquer le jeu à limites fixes jusqu’à ce que vous vous y sentiez à l’aise. Puis, risquez-vous de préférence sur les tables de micro-limites du poker sur Internet (voir le chapitre 16) ou dans les tournois à petit buy-in (voir le chapitre 17).

L’importance de votre capital
Vous devez payer pour jouer, mais ce petit jeu ne va pas durer longtemps si vous n’avez pas un budget préparé à l’avance pour survivre dans les périodes de vaches maigres.

Vous n’êtes pas obligé de jouer au poker pour gagner. C’est une bonne idée de passer votre temps à la table afin de vous entraîner, mais si vous jouez pour gagner, l’argent qui vous est disponible (votre capital) fera la différence entre le poker-divertissement et le fait de vous asseoir devant la télé.

 

Si vous n’avez jamais rencontré le concept de base de capital jeu auparavant, vous allez probablement être choqué de voir les montants que je vais évoquer ci-après. Alors respirez bien à fond. Méditez. Installez-vous à votre endroit préféré chez vous. Maintenant, allons-y...

Taille de capital recommandé
Le capital est ce qui va vous permettre de prospérer pendant les périodes fastes et de survivre aux périodes néfastes. Les fluctuations du hasard font que vous devez avoir un matelas pour amortir les chutes. Sans ce matelas, la chute est brutale et définitive.


La dure vérité du poker
Il est une vérité sur le poker que vous ne verrez jamais dans les shows télé glamour qui y sont consacrés. Vous verrez rarement aussi les acteurs de ces shows (les grands pros) la dévoiler, ce qu’ils ont à dire étant généralement une vision poétique du bon vieux temps. Cette vérité honteuse, la voici: la grande majorité des joueurs de poker perdent. Et quand je dis «perdent», c’est au sens strictement comptable du mot.

 

Il n’y a guère que quelques-uns qui osent traiter du sujet, comme par exemple le grand Amarillo Slim qui a prononcé un jour cette formule à l’emporte-pièce: «celui qui n’a pas tout perdu un jour au poker n’est pas un vrai joueur de poker».

 

Donc c’est un fait acquis: la grande majorité des joueurs de poker perdent. C’est-à-dire combien d’entre eux? La proportion est de l’ordre de... 95 %. C’est vrai: 19 joueurs sur 20 perdent de l’argent sur le long terme. Voilà qui vous donne une nouvelle perspective sur les foules qui jouent sur Internet et celles qui regardent les shows poker à la télé, pas vrai?

 

Comme je l’ai déjà évoqué, la plus grande part de cette perte est due au prélèvement (voir l’encadré ci-avant «Le prélèvement insidieux»), mais une autre revient à une mauvaise gestion du capital jeu. Si vous jouez pour gagner, même si vous êtes un joueur extrêmement talentueux, vous devez vous attendre à des ennuis sérieux si vous ne vous préparez pas totalement à affronter des revers de fortune.

Le capital pour le jeu en limites fixes

Si vous commencez comme un joueur de limites fixes, vous devez avoir un capital égal au moins à trois cents fois la taille maximum de la limite haute. Donc, pour une table à 2-4 euros, vous devez prévoir un capital de 1 200 euros.

 

Attention! Cela ne signifie en aucun cas que vous devez vous promener entre les tables de 2-4 avec 1 200 euros en poche! C’est même une chose que je vous demande de ne jamais faire ! Mais ce capital doit être celui que vous avez isolé pour le consacrer au jeu.

 

Il n’est pas inhabituel pour un débutant de perdre jusqu’à cinquante fois la limite haute au cours de ses premières heures de jeu. Un capital plus fort vous laissera repartir et gagner au-delà de cette perte.

 

Le capital pour le jeu en pot limit et en no limit

Comme vous vous en doutez, la nature brutale du jeu requiert ici un matelas plus fort encore que le jeu en limites fixes. Vous devrez posséder un capital minimum de dix fois le buy-in maximum pratiqué à votre table de prédilection. Par exemple, la table 1-2 proposera probablement un buy-in maximum à 200, donc votre capital devra être de 2000. Un capital de 4000 sera même plus sûr.

 

N.D.T.: C’est d’ailleurs cette limite que proposent certains théoriciens: ne jouez jamais plus de 5 % de votre capital jeu en une seule session de jeu. Si vous jouez au maximum un buy-in par session de jeu, dans notre exemple, votre capital doit donc être de 4 000. Mais si vous vous octroyez de jouer jusqu’à trois buy-ins par session de jeu, vous devez avoir un capital de 12 000. Et le reste à l’avenant.

 

Le capital pour le jeu en tournoi

 

Les tournois tendent à connaître des différences plus fortes – surtout les tournois multitables, où vous pouvez ne rien gagner pendant des semaines, puis gagner enfin une somme énorme qui vous remet le pied à l’étrier (pour plus de détails sur les tournois, voyez le chapitre 17).

Pour des tournois à une seule table, comme on en trouve à foison en ligne ou les satellites pour les gros tournois, comptez avoir cent fois le buy-in. Donc si vous jouez des tournois à une table à 10 euros, prévoyez un capital de 1000 euros.

 

Pour les tournois multitables, il vous faut encore plus car vos gains seront plus espacés et plus substantiels. Un minimum de 300 buy-ins est approprié (et n’oubliez pas de compter les rebuys). Donc, si vous jouez dans des tournois à 30 euros où vous recavez en moyenne deux fois, du point de vue de votre capital, cela revient à un seul tournoi à 90 euros. Ce qui signifie, si vous pratiquez ces tournois souvent, que vous devez prévoir un capital de... 9 000 euros. Quand je vous avais dit que votre pression artérielle pouvait monter en lisant ces lignes...

Monter et baisser les limites
En fonction de vos gains et de vos pertes, vous devez être capable de modifier les limites auxquelles vous jouez.

 

Comptabilisez

Pour devenir gagnant au sens comptable du mot, vous devez mettre en place l’outil comptable qui vous dira si oui ou non vous l’êtes, et de combien. Même s’il peut être amusant de noter aussi ce que vous avez mangé et bu, avec qui vous avez joué, la durée de la partie, le plus important reste évidemment de noter scrupuleusement vos gains et pertes, sans aucune omission.

 

N.D.T.: Je le réécris: sans aucune omission. Une perte dont vous êtes honteux ne doit pas être «oubliée». Vous devez avoir l’esprit assez froid du comptable pour la noter quand même. Personne d’autre que vous ne verra ces chiffres, donc notez-les. Il est plus important d’analyser pourquoi vous avez perdu une telle somme ce jour-là que d’en cacher le montant comme une maladie honteuse. La maladie honteuse, ce serait plus de voir un mauvais résultat se répéter.

 

Une feuille de papier suffit, ou encore un calepin à spirale. Vous y reporterez session après session vos gains et pertes.

 

Certains joueurs ont du mal à comprendre l’intérêt de cette tâche. Si deux joueurs ont exactement le même niveau technique, celui qui notera ses résultats gagnera plus car il est conscient au fil de la partie de l’état de son capital et il peut ajuster son style de jeu. Cette façon de présenter les choses prouve qu’un joueur peut en battre un autre en utilisant une technique qui n’a rien à voir avec les cartes!

 

La chute

 

Vous devez vous intéresser davantage encore à la perte qu’au gain. Vos résultats écrits en rouge dans votre calepin doivent vous faire réfléchir davantage que ceux écrits en vert. Si vous arrivez un jour à constater que vous avez perdu la moitié du capital que vous vous êtes assigné, vous devez accepter de diminuer vos mises, donc par exemple de passer de la table 2-4 à la table 1-2, où les différences moyennes sont plus faibles. Suivant le type de clé que vous utilisez, vous allez donc passer d’un capital de 1200 à 600 euros.

 

Mais ce n’est pas tout : si vos sessions perdantes se multiplient, il va aussi falloir examiner de près pourquoi vous avez perdu... Faire en quelque sorte un inventaire de vos carences – car tous les joueurs ont des carences, même les meilleurs, même si les meilleurs en ont moins que les autres.

Le cas de Max, ou comment devenir gagnant quand on note ses résultats
Mon copain Max est un fondu d’informatique et pendant ces dernières années, il s’est pris de passion pour le hold’em. Il a commencé par jouer régulièrement en ligne, tenant un livre de résultats au moins aussi méticuleux que les plus obsédés des serial killers.

 

Un jour, il vient me voir et me lance: «Je perds. Pas beaucoup, mais de façon durable.»

 

Nous nous sommes penchés ensemble sur ses résultats. Pas de doute, il perdait. Mais la perte était peu marquée qu’elle a piqué ma curiosité. «Eh, Max, peux-tu rectifier ces chiffres en y réintégrant le prélèvement?»

 

Ce qu’il a fait. Et cette fois, ses chiffres montraient clairement qu’il était gagnant. Le problème de Max était qu’il battait ses adversaires... mais qu’il n’arrivait pas à battre le prélèvement!

 

J’ai dit: «Si tu joues dorénavant sur la limite supérieure, le prélèvement va représenter un pourcentage plus faible de chaque pot (en effet, sur Internet, le prélèvement maximum est plafonné, généralement à 3 dollars par pot). Je parie que tu commenceras à redevenir gagnant.»

 

C’est exactement ce qu’il a fait... et c’est exactement ce qui s’est produit.

 

Max m’a offert un repas dans mon restaurant préféré. C’est ce que j’appelle vraiment gagner.

La montrée

 

Si vous êtes dans la position envieuse d’avoir doublé votre capital, vous pouvez peut-être jouer sur des tables plus chères. Certes, doubler votre capital représente beaucoup avant de monter en limites, mais cela prouvera sans aucun doute que vous êtes sur un trend chanceux et vous donnera à la fois l’expérience et la confiance dont vous avez besoin pour vous en sortir glorieusement au niveau supérieur.

Monter en limites implique toujours un jeu plus difficile. Si vous pensez qu’en jouant sur des tables plus chères vous allez automatiquement avoir le même taux de gains, préparez-vous à de sérieux maux de tête.

L’étiquette du poker
L’étiquette du poker va plus loin que d’être poli, d’être courtois et de ne pas casser votre chaise sur le donneur quand il vous retourne une carte perdante. Cela concerne aussi les rituels et les habitudes qui sévissent à la table. Ignorer de façon répétitive ces conventions peut conduire à vous faire exclure du club.

Tenir vos cartes
Pour jouer les cartes, vous devez d’abord savoir comment les tenir physiquement.

 Quand vous recevez vos cartes privatives, ne les tenez pas sous le bord de la table. Prenez-en connaissance rapidement, mémorisez-les puis protégez-les.
 Si les cartes utilisées sont en carton (comme celles que vous aviez quand vous jouiez gamin au jeu des «7 familles»), veillez à ne pas les plier pour ne pas les déformer. Si vous remarquez qu’un jeu comporte des déformations, signalez-le car cela peut permettre d’identifier certaines cartes. Les cartes plastique, comme les Kem ou les Copag, peuvent être pliées sans dommage.
 Gardez vos cartes découvertes. Cela permet au donneur et aux adversaires de voir que vous êtes toujours dans le coup.
 Ne déchirez pas vos cartes en deux quand vous perdez un coup à cause d’une carte «venue de l’espace» contre le type qui est sorti avec votre ex-nana et qui se moque de vous maintenant. Enfin, essayez.
 Ne jetez pas vos cartes avant votre tour. Quand vous passez, poussez vos cartes faces en bas vers le donneur. Quand vous les avez abandonnées de la sorte, vos cartes sont définitivement éliminées du coup et mortes.
Quand devez-vous montrer votre main?
Au hold’em, vous devez montrer votre main quand elle est meilleure que la main adverse. Si ce n’est pas le cas, rien ne vous oblige à le faire.

 

Vous pouvez avoir envie de la montrer quand même, par exemple quand elle est très forte ou quand vous avez été battu par une river chanceuse pour l’adversaire.

 

Souvenez-vous: quand vous abattez votre main, vous dévoilez en même temps une part de votre style de jeu. C’est comme si vous expliquiez à vos adversaires: «Vous voyez, avec ce type de main, je joue le coup de cette manière.»

 

Si cela ne vous gêne pas, et surtout si cela concourt à créer une image que vous souhaitez offrir (par exemple, «je ne joue que les meilleures mains – je ne bluffe jamais»), faites-le.

 

Mon conseil, que j’applique à mon cas personnel, est de ne jamais montrer ma main si je ne suis pas obligé de le faire. Je préfère simplement que mes adversaires restent dans le flou.

 

Ne montrez jamais votre main à quelqu’un pendant un coup. Cela inclut la fille au décolleté plongeant ou le type au physique de George Clooney qui est assis à votre droite et qui ne prend pas part au coup.

Manipuler ses jetons
Vous ne pouvez pas ignorer non plus les bases concernant vos jetons.

 Seuls les jetons qui sont sur la table sont éligibles au coup présent. Après la donne, vous n’avez pas le droit de sortir des jetons de votre poche pour enrichir votre tapis.
 En no limit, vous n’avez pas le droit de sortir des jetons de la table sauf si vous la quittez définitivement.
 En tournoi, vous devez empiler vos jetons proprement de façon que les adversaires et les superviseurs puissent évaluer le montant de votre tapis rapidement. Et quand vous migrez d’une table à une autre, vous devez les garder en pleine vue de tous.
 Quand vous voulez relancer, dites «Je relance» au donneur puis placez la mise sur la table. Si vous jouez en no limit, vous devez déplacer la totalité de votre relance en une seule fois si vous n’en avez pas indiqué clairement le montant avant. Poser les jetons pour suivre, puis en poser d’autres pour relancer, est totalement interdit (string bet, string raise).
 Posez vos mises devant vous, sans les mélanger aux mises du pot. De cette manière, le donneur sait exactement ce que chacun a misé. N’oubliez pas qu’un joueur peut relancer après vous et il est facile, alors, de savoir combien vous devez ajouter pour égaliser sa relance.
 Ne touchez pas les jetons d’un autre joueur. Même si votre aura brille de mille feux.
 Ne ramenez pas vers vous un pot tant que le donneur ne vous l’a pas clairement attribué.
 Ne misez pas quand ce n’est pas votre tour.
Jouer à son tour
Le poker est joué dans le sens des aiguilles d’une montre, tant pour ce qui concerne les cartes que pour les enchères. Préflop, le premier joueur à parler est celui qui est assis à gauche du surblindeur. Pour les trois autres tours d’enchères, le premier joueur à parler est celui qui est assis à gauche du bouton. Le joueur assis à votre droite doit parler avant vous. Si le joueur assis à votre gauche tend à parler avant vous, signifiez-lui gentiment qu’il doit se calmer et respecter l’ordre de parole.

 

Cette procédure n’est pas faite pour embêter les joueurs, mais pour éviter qu’un joyeux bazar ne s’installe à la table... et aussi pour éviter les manipulations de certains joueurs vicieux. Le fait de parler à son tour est encore plus crucial au stade de la river. Soyez certain que les enchères sont bien terminées et que personne n’attend pour prendre une décision avant de retourner vos cartes.

 

N.D.T.: Si vous croyez que tout le monde a passé et si vous jetez vos cartes en tendant les mains vers le pot, vous pouvez avoir la mauvaise surprise d’entendre un adversaire se plaindre: «Mais je n’ai pas encore parlé!» Si vos cartes ont touché d’autres cartes précédemment jetées, votre main est perdue, vous êtes éliminé d’office et votre adversaire seul en lice gagne le pot par défaut, quelle que soit sa main. Conclusion: quand vous croyez que tout le monde a passé sans en être totalement sûr, renseignez-vous. Et ne jetez jamais vos cartes tant que le donneur ne vous a pas attribué le pot.

Le pourboire
Dans les cash games, il est de coutume de donner un jeton de pourboire au donneur quand on gagne un pot. La plupart des donneurs ont leur salaire indexé sur les pourboires. Quand le pot est minuscule, personne ne vous en voudra de ne pas donner de pourboire. Mais quand il est énorme, on comprendra que le pourboire puisse être un peu plus élevé.

Garder un œil sur le jeu
Quand vous jouez au poker, spécialement si vous n’avez pas l’habitude de l’environnement, soyez curieux de ce qui se passe autour de vous. Vous pouvez vous adresser à deux membres du personnel: le donneur et le floorman.

 

Parler au donneur

 

Si vous rencontrez un problème à la table – que ce soit la manière dont une main a été annoncée ou une donne incorrecte – , parlez-en immédiatement au donneur. Le donneur est le chef de la table. Vous serez surpris de voir combien de joueurs s’improvisent shérifs quand il s’agit de résoudre un problème ou de trancher un différend. Ils n’ont pas la moindre autorité dans le club, mais le donneur, si. Encore une fois, parlez au donneur.

Quand quelqu’un flashe ses cartes
Vous serez surpris du nombre de joueurs qui ne couvrent pas assez leurs cartes et qui les laissent voir involontairement à leurs voisins. Neuf fois sur dix, c’est quelqu’un qui a trop bu ou qui s’en fiche complètement.

 

Je traite toujours le «problème» de la même façon. À la fin du coup, je dis assez fort pour que tout le monde entende: «Excusez-moi, mais vous devez protéger votre main – j’ai pu voir vos cartes cette fois.»

 

Cette stratégie obtient presque toujours un «merci» de la part du joueur incriminé et me met en règle avec ma conscience. Si le joueur expose encore sa main, je ne me sens pas coupable de me servir de cette information pour la façon dont je vais jouer le coup.

Demander l’avis d’un floorman

 

Mon expérience m’a montré que les donneurs commettent majoritairement deux types d’erreurs:

 En attribuant un pot à la mauvaise personne quand le tableau a été «condamné» (voir le chapitre 2).
 En se commettant trop dans les conversations des joueurs.
Si le donneur a commis une erreur – par exemple, s’il a attribué le pot au mauvais joueur ou autorisé une action qui vous semble clairement discutable –, appelez le floorman.

 

C’est une sorte d’arbitre qui se tient à la disposition des joueurs pour trancher les litiges et encadrer les donneurs.

 

Si vous avez vraiment besoin d’un floorman, l’ambiance à la table risque de s’enflammer ou de s’alourdir brusquement. Expliquez la situation au floorman et ne parlez que si on vous parle. N’argumentez pas avec les autres joueurs même s’ils s’adressent directement à vous.

Le hasard et vous
Si vous jouez depuis peu au poker, vous serez battu un jour par une carte «venue de l’espace». La question n’est pas de savoir si cela arrivera mais quand. Si vous avez du mal à admettre le concept de hasard, vous devriez envisager sérieusement de jouer aux échecs ou à un autre jeu où le hasard n’a pas sa place. (Vous pensez que la malchance vous poursuit? Alors jetez donc un œil aux gens qui hantent le chapitre 21.)

 

Cela n’amuse personne d’entendre un énième récit de bad beat, et si vous êtes débutant, vous devez comprendre que les gens assis autour de la même table sont passés par les mêmes affres. Le bad beat fait partie de la vie, il suffit de vivre avec et de l’accepter tel qu’il est.

À proscrire absolument: insulter un autre joueur, jeter les cartes à la figure du donneur, jeter votre verre de bière dans le miroir, déchirer les cartes de rage. Si le jeu devient trop bouillant ou trop intense, levez-vous, allez faire un tour, respirez l’air frais. Et n’oubliez pas qu’il y a toujours une autre partie ailleurs.

C’est juste un jeu, sauf si vous êtes un professionnel. Et si vous êtes un professionnel, vous devez vous comporter comme tel.