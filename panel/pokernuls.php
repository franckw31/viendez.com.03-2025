La première fois que vous verrez un coup de hold’em, vous risquez de ressentir la même chose que si vous étiez le seul à ne pas comprendre une plaisanterie. Ne vous en faites pas. Le hold’em est un jeu facile à comprendre.

 

Ce qui est moins évident, c’est la somme d’argent que vous allez amener à la table, de même que les conventions de jeu. C’est ce dont il est question ici.

L’ordre de parole
Au hold’em et comme dans toutes les variantes du poker, les cartes sont données dans le sens des aiguilles d’une montre. Certes, ce sens n’était pas la norme il y a encore quelques années partout en Europe, mais le standard est maintenant établi pour le monde entier. Après le premier tour de donne a lieu le premier tour d’enchères (voir le chapitre 1). Les joueurs qui décident de passer perdent évidemment tout droit au pot en cours et il leur est impossible de revenir en jeu pendant la suite du coup. Quand les cartes sont jetées, c’est irrémédiable.

Le bouton
Si vous avez l’habitude de jouer au poker sur un coin de la table de la cuisine, vous avez sans doute l’habitude de faire passer la donne au joueur suivant quand un coup vient de se terminer.

 

Dans les cercles officiels, la donne est assurée par un employé de la maison qui ne prend pas part au jeu. Ce donneur agit comme une sorte de robot-qui-distribue-les-cartes-suivant-une-procédure-précise-et-qui-gère-le-pot-suivant-une-autre-procédure-précise. Le cercle n’a pas d’intérêt dans le coup. Il gagne sa vie grâce au prélèvement que le donneur opère sur chaque pot (voir l’encadré «Le prélèvement insidieux»).

 

Quand les cartes sont données, le cercle agit comme si un des joueurs était lui-même donneur virtuel. Ce joueur a devant lui un jeton spécial généralement blanc marqué d’un «D» comme Donneur ou Dealer. Les cartes sont données à partir de ce joueur-là, exactement comme si c’était lui qui donnait physiquement les cartes. C’est aussi après le donneur que commencent les enchères.


Le prélèvement insidieux
Les cercles officiels n’ont pas d’intérêt financier dans les parties qu’ils organisent. Ils ne font que fournir les tables, les cartes, les jetons et les donneurs. Mais en échange, ils opèrent un prélèvement sur chacun des pots appelé aussi rake. Ce prélèvement est de 5 à 10 % de chaque pot, avec un plafond qui peut être de 3 euros par exemple sur la table 1-2.

 

À première vue, le prélèvement reste modique. Le perdant n’y voit pas d’inconvénient puisqu’il frappe un argent qu’il a déjà perdu. Et quand un gagnant reçoit 97 euros d’un pot qui en totalise 100, il n’a pas de quoi se plaindre.

 

Le problème est que le prélèvement a lieu sur tous les coups, inlassablement. Si dix joueurs s’assoient à la table avec 100 euros chacun et si 33 mains sont disputées, ce qui est généralement le cas en une heure de jeu, et si chacune est frappée de 3 euros de rake, 99 euros sont allés dans la poche du cercle en une heure. Soit l’équivalent d’un buy-in complet.

 

Pour prendre une comparaison, imaginez que vous lancez en l’air une pièce de monnaie. À chaque fois que face sort, vous payez 1 euro, et à chaque fois que pile sort, vous recevez 95 centimes. C’est exactement ce qui se produit avec le rake de la table du cercle.

 

La seule façon de contourner le prélèvement est de jouer chez soi dans une partie privée sans prélèvement. Sinon, votre capital est constamment menacé.

Le fait que le bouton passe de joueur en joueur après chaque coup, dans le sens des aiguilles d’une montre, garantit que chaque joueur sera bouton chacun à son tour. (Pour savoir pourquoi c’est une bonne affaire, voyez le chapitre 1.)

Distribuer les cartes
L’ordre dans lequel le hold’em est donné est spécifique et, en supposant que vous jouez avec des gens lucides et honnêtes, il ne changera jamais.

Les cartes privatives
À partir du bouton, tous les joueurs reçoivent, une à une, deux cartes privatives, donc données faces en bas dans le sens des aiguilles d’une montre. Vous avez le droit de regarder ces cartes (en fait, vous devez le faire, sauf dans le cas rare où vous n’en avez pas besoin grâce à vos capacités psychiques suffisantes qui vous garantissent une divination fiable à cent pour cent). Cependant, rien ne vous oblige à les montrer aux adversaires, même quand vous décidez de passer en cours de coup. Vous en saurez plus en allant à la section «Étiquette du poker» plus loin dans ce chapitre.

Veillez à ne pas laisser voir vos cartes privatives au moment où vous en prenez connaissance. Vous avez le droit d’utiliser vos deux mains pour ce faire, mais vous n’avez pas le droit de faire sortir vos cartes du périmètre de la table. Quand vous prenez connaissance de vos cartes, à vous de les couvrir suffisamment pour éviter que des yeux baladeurs ne les flashent.

 

Quand vos yeux identifient vos cartes, pensez à les mémoriser pour ne pas avoir à les regarder une deuxième fois en cours de coup. Chaque carte s’identifie par deux éléments : sa valeur et sa couleur. Par exemple, huit et cœur. Si un tirage à quinte ou à couleur apparaît au flop et si vous avez correctement identifié vos cartes, vous n’aurez pas à les regarder à nouveau, ce qui vous fera gagner du temps et vous évitera de perdre le fil de votre concentration.

 

N.D.T.: Un «truc»: quand vous identifiez vos cartes, mémorisez-les par ordre décroissant, puis les deux couleurs dans le même ordre. Par exemple, la main K♠-Q♦ se signale mentalement: «roi, dame, pique, carreau». En respectant cette procédure, vous aurez de façon routinière votre main en «photo» mentale et elle ne vous échappera pas jusqu’à la fin du coup.

Quand vous venez de recevoir vos cartes fermées, ce n’est pas une mauvaise idée de ne pas en prendre connaissance tout de suite. Il est même conseillé de ne les consulter qu’au moment où on doit parler. Cette façon de faire présente deux avantages:

 Comme vous ignorez vos cartes, vous ne pouvez rien trahir les concernant tant que vous n’avez pas parlé;
 Comme vous ne pensez pas à vos cartes en attendant votre tour de parler, vous avez l’esprit libre pour observer vos adversaires et voir s’ils ne trahissent pas quelque chose concernant leurs cartes.
Si vous décidez d’attendre que ce soit votre tour de parler pour regarder vos cartes privatives, presque tous les joueurs auront l’œil braqué sur vous pendant que vous regarderez vos cartes et réfléchirez à votre action. C’est le moment d’exhiber votre meilleure poker face, c’est-à-dire un visage impassible. Si vous n’avez pas la faculté de créer une bonne poker face, vous avez encore la ressource de regarder vos cartes une à une au fur et à mesure qu’elles vous arrivent – vous pouvez aussi porter un masque d’Halloween.


Protégez votre main
Quand vous jouez dans un environnement de type casino ou cercle, protéger votre main est essentiel. Cela signifie que vous devez avoir un contrôle parfait sur elles pour que le donneur ne puisse pas les confondre avec des cartes jetées. Par exemple, vous pouvez poser vos mains dessus – ce n’est pas la meilleure solution car il n’y a pas de raison a priori pour que vos mains restent immobiles.

 

Vous pouvez aussi placer un petit objet sur vos cartes, ou une pile de jetons, ou les clés de votre Cadillac, voire même votre nièce que votre belle-sœur vous avait chargé de garder pendant la journée (si vous savez qu’elle se tiendra tranquille à table). Greg Raymer, gagnant des World Séries of Poker 2004, utilise des fossiles. J’utilise pour ma part un zloty polonais car on ne peut le confondre avec aucun autre objet.

 

Si vous ne protégez pas votre main et si vos cartes sont touchées par des cartes provenant de joueurs qui viennent de passer, votre main est immédiatement annulée. Autre possibilité: le donneur pense à tort que vous avez passé vous-même, donc il ramasse de bonne foi vos deux cartes privatives. Vous devriez être capable de nommer votre main pour l’extraire du tas de cartes pêle-mêle qui se trouve devant vous.

 

Prenez l’habitude de poser un objet sur vos cartes privatives. Quand vous passez, donnez-les face en bas au donneur et laissez votre objet bien en vue devant vous.

Le flop
Après le tour d’enchères concernant les cartes privatives (voir «Enchérir», plus loin dans ce chapitre), la carte suivante est brûlée: le donneur la pose face en bas sur la table, elle ne sera pas découverte et n’aura aucun rôle dans la combinaison finale. Le donneur étale trois cartes côte à côte et faces en haut au milieu de la table, devant lui. C’est ce qu’on appelle le flop, c’est-à-dire le début du tableau final constitué des cartes communes.

 

Ceux qui n’ont pas passé possèdent maintenant cinq cartes – deux cartes privatives et trois cartes communes. Un deuxième tour d’enchères a lieu.

La turn
Après le tour d’enchères du flop, une autre carte est brûlée à partir du talon et une quatrième carte est exposée. On l’appelle la turn.

 

Tous les joueurs qui sont encore en jeu possèdent maintenant six cartes, parmi lesquelles ils doivent identifier leur meilleure combinaison de cinq cartes possible. Un troisième tour d’enchères a lieu.

La river
Une carte est brûlée et la plus critique des cartes communes, la river, est exposée. Les joueurs encore en jeu possèdent maintenant sept cartes chacun : leurs deux cartes privatives combinées aux cinq cartes du tableau central. Le dernier tour d’enchères a lieu. À l’abattage, la meilleure main de cinq cartes gagne le coup et son détenteur encaisse le pot.

 

Pour déterminer leur main de cinq cartes parmi leurs sept cartes, les joueurs peuvent utiliser aucune, une ou deux cartes de leur main propre, respectivement avec cinq, quatre ou trois cartes du tableau.

Comme un joueur doit utiliser au moins trois cartes communes pour former sa combinaison finale, il ne peut pas y avoir de couleur s’il n’y a pas au moins trois cartes de même couleur au tableau. De la même manière, il ne peut pas y avoir de quinte s’il n’y a pas au tableau au moins trois cartes qui appartiennent à la même quinte (c’est le cas dans le tableau suivant). On qualifie de rainbow un flop ou un tableau où les différentes familles sont représentées, sans possibilité de couleur.

