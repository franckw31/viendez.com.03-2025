L’abattage
Si tout le monde a passé en cours de coup, ce qui ne laisse en place qu’un seul joueur, ce joueur-là gagne automatiquement le coup. Rien ne l’oblige à abattre son jeu car il n’a pas à prouver sa force en cartes.

 

S’il reste plus d’un joueur dans le coup et si le dernier tour d’enchères a eu lieu, on passe alors à l’abattage, où les joueurs retournent leurs cartes pour comparer leurs combinaisons.

 

Tout le monde devrait logiquement retourner ses cartes, mais il se trouve que des gens sont réticents à le faire. Si personne ne fait le geste fatidique, c’est au dernier relanceur (ou au dernier ouvreur si personne n’a relancé) de le faire en premier. Si personne n’a enchéri au dernier tour d’enchères, c’est au joueur qui se trouve le plus proche du donneur de retourner sa main en premier.

 

Les joueurs ont intérêt alors à ne pas retourner leur main s’ils constatent que la main retournée avant eux est meilleure. Quand une main a été jetée, elle est considérée comme morte quoi qu’il arrive et ne peut plus en aucun cas prétendre à une part du pot.


Jeter un œil aux cartes d’un adversaire
Il est un outil méconnu mais parfois efficace à utiliser quand vous jouez au hold’em. Si un joueur a payé le coup jusqu’à la river puis perd à l’abattage, vous avez le droit de demander à voir sa main, même s’il entend la jeter parce que l’adversaire en a abattu une meilleure.

 

Pour la voir, faites-le savoir au donneur le plus tôt possible. Même si le joueur visé jette ses cartes devant le donneur, celui-ci doit isoler les deux cartes et les retourner faces en l’air en précisant bien que cette main est morte pour ce coup (parce que la main peut finalement être gagnante, si celui qui l’a jetée l’a mal lue; étant décrétée morte, même meilleure que la main adverse, elle ne peut plus gagner le pot).

 

Les règles varient. Dans tous les clubs, un joueur doit avoir pris part au coup pour disposer de ce droit. Donc un observateur n’a en aucun cas ce droit. Dans certains clubs, vous devez être resté vous-même dans le coup jusqu’au bout. Dans d’autres, ce droit vous est acquis dès l’instant que vous avez reçu des cartes dans ce coup, même si vous avez passé préflop.

 

Mais cette règle comporte une nuance. Si c’est le gagnant du coup qui demande d’exercer ce droit de regard, il court le risque de se faire battre si la main retournée s’avère meilleure que la sienne! Cela paraît incroyable, mais c’est pourtant la règle qui prévaut. Cela étant, après des années de jeu, je n’ai jamais vu une telle chose se produire.

 

Ne soyez pas surpris de constater des réactions désapprobatrices à la table si vous demandez à voir cette fameuse main. Pour une raison que je ne m’explique pas, cette demande légitime est très impopulaire.

 

Dans les parties en ligne, cette demande est plus simple. Dans presque tous les sites, quand vous demandez l’historique d’une main, celui-ci vous montrera toutes les mains qui sont allées jusqu’à l’abattage. (Pour plus de détails sur le jeu en ligne, voyez le chapitre 15.)

Quand la première main a été retournée, les joueurs qui restent retournent la leur dans le sens des aiguilles d’une montre. Le donneur déclare mortes les mains les unes après les autres tant qu’elles sont inférieures à la main déjà exposée, et quand tous les joueurs ont retourné ou jeté leur main, il ne reste sur la table, faces découvertes, que la main gagnante. C’est bien sûr vers son propriétaire que le donneur dirige le pot.

Ne jetez jamais votre main si un joueur annonce une main supérieure à la vôtre. Avant de jeter votre main, constatez sur pièces la véracité de son annonce. Car s’il annonce une combinaison supérieure à la vôtre et si vous jetez immédiatement votre main, celle-ci est déclarée morte ; imaginez maintenant que, consciemment ou non, l’adversaire ait annoncé une combinaison supérieure à celle qu’il détient vraiment, c’est quand même lui qui va gagner le pot car votre main aura perdu tout droit au pot du fait que vous l’aurez jetée.

 

J’ai bien dû voir cent fois dans ma vie des gens annoncer «couleur» alors qu’ils avaient une combinaison de quatre piques et trois trèfles, ou quatre cœurs et trois carreaux.

 

Conclusion: quand l’adversaire annonce une combinaison, attendez qu’il étale son jeu avant de jeter le vôtre ensuite. Et si, après avoir vu la combinaison adverse, vous n’êtes toujours pas certain d’avoir perdu, retournez vos cartes: le donneur vous dira ce qu’il en est.

Des blinds à poster
Pour commencer les enchères au hold’em, les mises forcées appelées blinds sont postées par deux joueurs. Le joueur assis à gauche du bouton poste le petit blind, et la personne assise à gauche du petit blind poste le gros blind ou surblind. On poste les blinds avant que la première carte soit donnée.

La taille des enchères dépend des limites qui ont été décidées. Le petit blind est généralement égal à la moitié du surblind (pour en savoir plus, lisez «Enchérir», plus loin dans ce chapitre). Au hold’em à limites fixes 2-4 euros, les blinds sont de 1-2.

Les blinds sont des mises forcées. Les joueurs qui sont en position de blindeurs doivent payer les blinds, que cela leur plaise ou non, sans quoi ils ne recevront pas de cartes. Ces blinds ont pour fonction d’activer les mises après que les joueurs ont reçu leurs cartes.

 

